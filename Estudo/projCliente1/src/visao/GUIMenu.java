    private void abrirCadPessoa() {
        if (!flagCadCliente) {
            GUICadCliente cadCliente = new GUICadCliente();
            jAreaDeTrabalho.add(cadCliente);
            cadCliente.setVisible(true);
            flagCadCliente = true;
            cadCliente.addInternalFrameListener(this);
        }//fecha if

    }

    private void abrirJanelaTable() {
        if (!flagBuscaCliente) {
            GUIBuscarCliente gmp = new GUIBuscarCliente();
            jAreaDeTrabalho.add(gmp);
            gmp.setVisible(true);
            flagBuscaCliente = true;
            gmp.addInternalFrameListener(this);
        }
    
        
    }//fecha m√©todo

