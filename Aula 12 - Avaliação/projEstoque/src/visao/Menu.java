    private void abrirCadProduto() {
        if (!flagCadProduto) {
            UICadVonpar cadProduto = new UICadVonpar();
            jAreaTrabalho.add(cadProduto);
            cadProduto.setVisible(true);
            flagCadProduto = true;
            cadProduto.addInternalFrameListener(this);
        }//fecha if

    }

    private void abrirJanelaTable() {
        if (!flagBuscaProduto) {
            UIBuscaProduto gmp = new UIBuscaProduto();
            jAreaTrabalho.add(gmp);
            gmp.setVisible(true);
            flagBuscaProduto = true;
            gmp.addInternalFrameListener(this);
        }

    }

